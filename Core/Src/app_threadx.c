/**
  * @brief  Application ThreadX Initialization.
  * @param memory_ptr: memory pointer
  * @retval int
  */
UINT App_ThreadX_Init(VOID *memory_ptr)
{
  UINT ret = TX_SUCCESS;
  TX_BYTE_POOL *byte_pool = (TX_BYTE_POOL*)memory_ptr;

  /* USER CODE BEGIN App_ThreadX_Init */
  static const ULONG app_memory_size = 1024;
  VOID *pointer;
  ret = tx_byte_allocate(byte_pool, &pointer, app_memory_size, TX_NO_WAIT);
  if (ret != TX_SUCCESS) {
    SEGGER_RTT_printf(0, "tx_byte_allocate failed\n");
    Error_Handler();
  }

  ret = tx_thread_create(
    &app_thread, "app_thread", app_thread_entry, 0,
    pointer, app_memory_size, 
    1, 1, 1, TX_AUTO_START
  );
  /* USER CODE END App_ThreadX_Init */

  return ret;
}

